template<int V, int P>
constexpr MInt<P> CInv = MInt<P>(V).inv();
 
constexpr int P = 1'000'000'007; // 998244353;
using Z = MInt<P>;

constexpr int m = 6;

class Solution {
public:
    int distinctSequences(int n) {
        vector dp(n + 1, vector(m + 1, vector<Z>(m + 1)));
        vector tdp(n + 1, vector<Z>(m + 1));
        for (int i = 1; i <= m; i++) {
            dp[1][i][0] = 1;
            tdp[1][i] += 1;
        }

        auto is_coprime = [&](int x, int y) {
            return gcd(x, y) == 1;
        };

        for (int i = 2; i <= n; i++) {
            for (int j = 1; j <= m; j++) {
                for (int k = 1; k <= m; k++) {
                    if (is_coprime(j, k)) {
                        dp[i][j][k] = tdp[i - 1][k] - dp[i - 1][k][j];
                        tdp[i][j] += dp[i][j][k];
                    }
                }
            }
        }
    }
};

